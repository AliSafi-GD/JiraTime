const dicts: Record<Lang, Dict> = { fa, en };

export type T = (key: string, vars?: Vars) => string;

export function makeT(lang: Lang): T {
  const d = dicts[lang] ?? fa;
  return (key, vars) => {
    let s = d[key] ?? key;
    if (vars)
      for (const k of Object.keys(vars)) s = s.replace(`{${k}}`, String(vars[k]));
    return s;
  };
}

export const isRTL = (lang: Lang) => lang === "fa";
